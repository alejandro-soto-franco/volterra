use volterra_core::ActiveNematicParams;
use volterra_fields::QField2D;
use volterra_solver::run_active_nematic_hydro;

// Fixed tiny problem: 8x8, 6 steps, snapshot every 2, noise on.
fn fixture() -> (QField2D, ActiveNematicParams) {
    let mut p = ActiveNematicParams::default_test();
    p.nx = 8;
    p.ny = 8;
    p.noise_amp = 0.01;
    let q = QField2D::random_perturbation(p.nx, p.ny, p.dx, 0.001, 42);
    (q, p)
}

fn flatten(q: &QField2D) -> Vec<f64> {
    q.q.iter().flat_map(|[a, b]| [*a, *b]).collect()
}

#[test]
fn wet_2d_matches_captured_oracle_bit_for_bit() {
    let (q0, p) = fixture();
    let (qf, stats) = run_active_nematic_hydro(&q0, &p, 6, 2);
    let field = flatten(&qf);
    let oracle_field: &[f64] = &ORACLE_FIELD;
    let oracle_means: &[f64] = &ORACLE_MEANS;
    for (i, (a, b)) in field.iter().zip(oracle_field).enumerate() {
        assert_eq!(a, b, "final field drifted at index {i}");
    }
    assert_eq!(field.len(), oracle_field.len(), "field length mismatch");
    for (i, (s, m)) in stats.iter().zip(oracle_means).enumerate() {
        assert_eq!(&s.mean_s, m, "mean_s drifted at snapshot {i}");
    }
    assert_eq!(stats.len(), oracle_means.len(), "snapshot count mismatch");
}

