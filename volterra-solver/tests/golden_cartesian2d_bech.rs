use volterra_core::ActiveNematicParams;
use volterra_fields::{QField2D, ScalarField2D};
use volterra_solver::run_bech;

// Fixed tiny problem: 8x8, 6 steps, snapshot every 2.
fn fixture() -> (QField2D, ScalarField2D, ActiveNematicParams) {
    let mut p = ActiveNematicParams::default_test();
    p.nx = 8;
    p.ny = 8;
    p.noise_amp = 0.01;
    let q = QField2D::random_perturbation(p.nx, p.ny, p.dx, 0.001, 42);
    // phi near equilibrium with small sinusoidal perturbation, matching the
    // existing ch_step_etd_output_finite test construction.
    let phi_vals: Vec<f64> = (0..8 * 8)
        .map(|k| {
            let frac = k as f64 / (8.0 * 8.0);
            0.5 + 0.05 * (frac * 7.3).sin()
        })
        .collect();
    let phi = ScalarField2D { phi: phi_vals, nx: 8, ny: 8, dx: p.dx };
    (q, phi, p)
}

fn flatten_q(q: &QField2D) -> Vec<f64> {
    q.q.iter().flat_map(|[a, b]| [*a, *b]).collect()
}

#[test]
fn bech_2d_matches_captured_oracle_bit_for_bit() {
    let (q0, phi0, p) = fixture();
    let (qf, phif, stats) = run_bech(&q0, &phi0, &p, 6, 2);

    let q_field = flatten_q(&qf);
    for (i, (a, b)) in q_field.iter().zip(ORACLE_Q_FIELD.iter()).enumerate() {
        assert_eq!(a, b, "final Q field drifted at index {i}");
    }
    assert_eq!(q_field.len(), ORACLE_Q_FIELD.len(), "Q field length mismatch");

    for (i, (a, b)) in phif.phi.iter().zip(ORACLE_PHI_FIELD.iter()).enumerate() {
        assert_eq!(a, b, "final phi field drifted at index {i}");
    }
    assert_eq!(phif.phi.len(), ORACLE_PHI_FIELD.len(), "phi field length mismatch");

    for (i, (s, m)) in stats.iter().zip(ORACLE_MEAN_S.iter()).enumerate() {
        assert_eq!(&s.mean_s, m, "mean_s drifted at snapshot {i}");
    }
    for (i, (s, m)) in stats.iter().zip(ORACLE_MEAN_PHI.iter()).enumerate() {
        assert_eq!(&s.mean_phi, m, "mean_phi drifted at snapshot {i}");
    }
    assert_eq!(stats.len(), ORACLE_MEAN_S.len(), "snapshot count mismatch");
}

